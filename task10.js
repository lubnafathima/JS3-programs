// 10. WAP that takes a month and prints the corresponding season: "Spring," "Summer," "Autumn," or "Winter."
let month = "July";
month = month.toLowerCase();
if (month === "december") {
  console.log("Winter");
} else if (month === "january") {
  console.log("Winter");
} else if (month === "february") {
  console.log("Winter");
} else if (month === "march") {
  console.log("Spring");
} else if (month === "april") {
  console.log("Spring");
} else if (month === "may") {
  console.log("Spring");
} else if (month === "june") {
  console.log("Summer");
} else if (month === "july") {
  console.log("Summer");
} else if (month === "august") {
  console.log("Summer");
} else if (month === "september") {
  console.log("Autumn");
} else if (month === "october") {
  console.log("Autumn");
} else if (month === "november") {
  console.log("Autumn");
} else {
  console.log("Invalid month");
}